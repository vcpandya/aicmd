    def is_done(self, response) -> bool:
        """Check if done — works with both CommandResponse and string."""
        if isinstance(response, CommandResponse):
            return response.is_done or DONE_SENTINEL in response.command
        return DONE_SENTINEL in str(response)

    def _handle_api_error(self, e: Exception) -> None:
        msg = str(e)
        if "API key" in msg or "401" in msg or "403" in msg:
            raise SystemExit("Invalid API key. Run 'zx setup' to reconfigure.")
        if "429" in msg:
            raise SystemExit("Rate limited by API. Please wait and try again.")
        if "404" in msg:
            raise SystemExit(f"Model not found. Check your model name in config. Error: {msg}")
        raise SystemExit(f"AI error: {msg}")
