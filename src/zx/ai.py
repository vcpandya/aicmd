# ── Pydantic schemas for structured output ───────────────────────────────────


class CommandResponse(BaseModel):
    """Structured response from the AI for command generation."""
    command: str  # The shell command to execute, or "__DONE__" if objective is complete
    explanation: str  # Brief one-line explanation of what this command does
    is_done: bool  # True if the objective is fully complete


class ExplainResponse(BaseModel):
    """Structured response for command explanation."""
    summary: str  # One-line summary of what the command does
    breakdown: list[str]  # Step-by-step breakdown of each part
    risks: str  # Side effects or risks (empty string if none)


class PlanStep(BaseModel):
    """A single step in an execution plan."""
    step_number: int
    command: str
    explanation: str
    is_reversible: bool
    undo_command: str  # empty string if not reversible


class PlanResponse(BaseModel):
    """Full execution plan generated by AI."""
    summary: str  # one-line plan overview
    steps: list[PlanStep]
    warnings: str  # caveats or empty string


class PlanAdaptResponse(BaseModel):
    """AI response when adapting a plan after failure."""
    assessment: str  # what went wrong
    revised_steps: list[PlanStep]
    should_abort: bool
    abort_reason: str  # empty string if not aborting


class DiagnosisResponse(BaseModel):
    """AI response for diagnosing command failures."""
    root_cause: str       # Plain-English explanation of what went wrong
    fix_command: str      # The command to fix the issue
    explanation: str      # What the fix does
    prevention_tip: str   # How to prevent this in the future
    confidence: str       # "high", "medium", or "low"


class RecipeParameterization(BaseModel):
    """AI response for identifying parameters in a recipe."""
    parameters: list[str]               # e.g. ["project_name", "python_version"]
    parameterized_commands: list[str]    # commands with {{param}} placeholders


class AliasEntry(BaseModel):
    """A single alias suggestion."""
    name: str
    command: str
    reason: str


class AliasSuggestion(BaseModel):
    """AI response for alias suggestions."""
    aliases: list[AliasEntry]


class LessonResponse(BaseModel):
    """AI response for a shell tutorial lesson."""
    explanation: str       # Main lesson content
    examples: list[str]    # Example commands to try
    exercise: str          # A practice exercise
    tips: list[str]        # Helpful tips


class PlaybookGeneration(BaseModel):
    """AI response for generating playbook metadata from a fix session."""
    symptoms: list[str]                  # Observable signs of the problem
    diagnostic_commands: list[str]       # Commands to diagnose the issue
    diagnostic_explanations: list[str]   # What each diagnostic command checks
    what_to_look_for: list[str]          # What to look for in diagnostic output
    prevention_tips: list[str]           # How to prevent this in the future
    category: str                        # "security", "debugging", "system"
    severity: str                        # "critical", "high", "medium", "low"
    tags: list[str]                      # Categorization tags


# ── Shell / environment detection ────────────────────────────────────────────


def _detect_shell_info() -> dict:
    info = {
        "os_name": platform.system(),
        "os_version": platform.version(),
        "cwd": os.getcwd(),
        "home_dir": str(Path.home()),
    }

    if platform.system() == "Windows":
        if os.environ.get("PSModulePath"):
            info["shell"] = "powershell"
        else:
            info["shell"] = "cmd"
    else:
        info["shell"] = os.environ.get("SHELL", "/bin/bash")

    return info


def _detect_project_context() -> str:
    cwd = Path.cwd()
    detected = []
    for marker, label in PROJECT_MARKERS.items():
        if (cwd / marker).exists():
            detected.append(label)
    if not detected:
        return "No specific project type detected."
    return "Detected project types: " + ", ".join(sorted(set(detected)))


